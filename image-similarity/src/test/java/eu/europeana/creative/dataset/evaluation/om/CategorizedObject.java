package eu.europeana.creative.dataset.evaluation.om;

public class CategorizedObject {

	protected String contentClass;
	protected String contentSubClass;

	public CategorizedObject() {
		super();
	}

	public String getContentClass() {
		return contentClass;
	}

	public void setContentClass(String contentClass) {
		this.contentClass = contentClass;
	}

	public String getContentSubClass() {
		return contentSubClass;
	}

	public void setContentSubClass(String contentSubClass) {
		this.contentSubClass = contentSubClass;
	}

}